package com.example.advjava.general;

public class SOLIDPrinciples {

    /**
     * Single Responsibility Principle
     * A class should have one and only one responsibility and reason to change
     */

    /**
     * Open Closed Principle
     * A class should be open for extension, but closed for modification
     * ex. leverage Polymorphism instead of using if and switch statements
     */

    /**
     * Liskov Substitution Principle
     * Subtypes should be replaceable by their base types
     * Better use composition instead of inheritance if this in not the case
     */

    /**
     * Interface Segregation Principle
     * Many specific interfaces are better than one general interface
     */

    /**
     * Dependency Inversion Principle
     * We must depend on abstractions and not on concrete classes
     * ex. services should call abstractions (interfaces)
     * Same applies for modules nad packages
     */

}
