package com.example.advjava.general;

public class OOPConcepts {

    /**
     * Class
     * is a blueprint for creating objects (a particular data structure), providing initial values for
     * - state (member/instance variables or attributes), and
     * - implementations of behavior (member/instance functions or methods)
     */

    /**
     * Object
     * is the instantiation of a class. a particular data structure that has unique:
     * - state (member/instance variables or attributes), and
     * - behavior (member/instance functions or methods)
     */

    /**
     * Static
     * This keyword in Java indicates that a particular attribute/method is not an instance,
     * but rather part of a type.
     * So, they are called class variables/methods
     * The static member will be shared among all instances of the class, so we will only create one instance of it
     */

    /**
     * Coupling
     * is a measure of how much a class is dependent on other classes.
     * There should be minimal dependencies between classes
     */

    /**
     * Cohesion
     * is a measure of how related the responsibilities of a class are
     * There should be high cohesion
     */

    /**
     * Encapsulation
     * is the concept of binding fields (object state) and methods (behavior) together as a single unit
     * hidden implementation, doesn't affect or cannot be affected by other classes changes
     */

    /**
     * Inheritance
     * one class is allowed to hase the features (fields and methods) of another class
     * promotes code reuse and establishes relationships between classes
     * IS-A relationship
     */

    /**
     * Polymorphism
     * is ability of an object to take on many forms and different behaviours
     */

    /**
     * Association
     * represents the relationship between two classes (unidirectional or bidirectional)
     * HAS-A relationship
     */

    /**
     * Aggregation
     * Association subtype
     * dependent obj is standalone and can exist if the owning class is dead - loose coupling
     */

    /**
     * Composition
     * Association subtype
     * dependent obj cannot exist if the owning class is dead - tight coupling
     */

}
